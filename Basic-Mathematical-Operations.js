function basicOp(operation, value1, value2)
// Check if operation truly equals to value 1 and value 2
// Return the results of values
{
if(operation === '+'){
  return value1 + value2
} else if (operation === '-'){
  return  value1 - value2 
} else if (operation === '*'){
  return value1 * value2
}else (operation === value1/value2)
  return value1/value2
}